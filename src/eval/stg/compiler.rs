/// Errors found during compilation
#[derive(Debug, Error)]
pub enum CompileError {
    #[error("unresolved variable '{1}'")]
    FreeVar(Smid, String),
    #[error("unresolvable bound variable reference")]
    BoundVarOverflowsContext,
    #[error("exceed maximum number of lambda args")]
    MaxLambdaArgs,
    #[error("unknown intrinsic {0}")]
    UnknownIntrinsic(String),
    #[error("encountered an uncompileable expression (operator soup)")]
    BadSoupExpression(Smid),
    #[error("encountered an uncompileable expression (arg tuple)")]
    BadArgTupleExpression(Smid),
    #[error("internal compiler error: unexpected expression during STG compilation")]
    UnexpectedExpression(Smid),
}

impl HasSmid for CompileError {
    fn smid(&self) -> Smid {
        use self::CompileError::*;

        match *self {
            FreeVar(s, _) => s,
            BadSoupExpression(s) => s,
            BadArgTupleExpression(s) => s,
            UnexpectedExpression(s) => s,
            _ => Smid::default(),
        }
    }
}

impl CompileError {
    pub fn to_diagnostic(&self, source_map: &SourceMap) -> Diagnostic<usize> {
        let diag = source_map.diagnostic(self);
        match self {
            CompileError::FreeVar(_, name) => {
                // Detect bindings that are internal to their unit (marked
                // `export: :internal`) and give a specific error message.
                if let Some(real_name) = name.strip_prefix(INTERNAL_FREE_VAR_PREFIX) {
                    let friendly_diag = source_map
                        .diagnostic(self)
                        .with_message(format!("unresolved variable '{}'", real_name));
                    return friendly_diag.with_notes(vec![format!(
                        "note: '{}' is marked `export: :internal` in its unit and \
                         cannot be accessed by importers",
                        real_name
                    )]);
                }
                let mut notes = vec!["check that the variable is defined and in scope".to_string()];
                // Detect mangled anaphoric parameter names that indicate a '_' was used
                // in a context where it is out of scope (e.g. inside function call
                // arguments where the anaphor scope does not extend).
                // Internal names of the form '_e_a[N]', '_e_n0', '_e_il0' etc. are
                // generated by the desugarer when expression anaphora are cooked.
                if name.starts_with("_e_") || name.starts_with("_e[") {
                    // Replace the confusing internal name with a user-friendly message.
                    let friendly_diag = source_map
                        .diagnostic(self)
                        .with_message("anonymous function parameter ('_') is out of scope");
                    notes.push(
                        "note: this error often means '_' was used in a position where \
                         its anonymous-function scope does not extend"
                            .to_string(),
                    );
                    notes.push(
                        "hint: 'f(_)' does not create a section — use a named function \
                         or pass the function directly, e.g. 'map(double)' not \
                         'map(double(_))'"
                            .to_string(),
                    );
                    return friendly_diag.with_notes(notes);
                }
                // If the name is a single letter it almost certainly comes from an
                // arrow-function expression like `x -> x + 1`.  Avoid showing this
                // hint for longer names (e.g. `xs`, `zdt`) that are more likely
                // genuine unresolved identifiers rather than misused lambda parameters.
                if name.len() == 1 && name.chars().all(|c| c.is_alphanumeric()) {
                    notes.push(
                        "note: eucalypt has no arrow functions; '->' is the const \
                         operator, not lambda syntax"
                            .to_string(),
                    );
                    notes.push(
                        "use anaphora (_ + 1), sections (+ 1), or named functions \
                         instead"
                            .to_string(),
                    );
                }
                // Suggest eucalypt equivalents for common operators from other
                // languages.
                if name == "==" {
                    notes.push("eucalypt uses '=' for equality, not '=='".to_string());
                }
                // Detect assignment-style declarations (e.g. `x = 42`) which
                // are not valid eucalypt syntax: eucalypt uses `:` for
                // declarations, not `=`.
                if name == "=" {
                    notes.push(
                        "note: eucalypt uses ':' for declarations, not '='; \
                         write 'x: 42' instead of 'x = 42'"
                            .to_string(),
                    );
                }
                // Also handle `+=`, `-=`, etc. from other languages
                if matches!(name.as_str(), "+=" | "-=" | "*=" | "/=" | "%=") {
                    notes.push(
                        "note: eucalypt has no compound assignment operators; \
                         eucalypt values are immutable — bind a new name with ':'"
                            .to_string(),
                    );
                }
                // Hint for import-style idioms from other languages.
                // In eucalypt, imports use metadata attached to the importing block.
                if matches!(
                    name.as_str(),
                    "import" | "use" | "require" | "include" | "load"
                ) {
                    notes.push(
                        "note: eucalypt does not have an import statement; \
                         use import metadata to bring a file into scope in a block"
                            .to_string(),
                    );
                    notes.push(
                        "example: ` { import: path/to/file.eu }  my-block: { ... }".to_string(),
                    );
                }
                // Suggest eucalypt equivalents for functions common in other languages
                // that have different names or do not exist in eucalypt.
                match name.as_str() {
                    "fold" | "reduce" | "foldLeft" | "foldRight" => {
                        notes.push(
                            "to fold a list, use 'foldl' (left fold) or 'foldr' (right fold), \
                             e.g. 'foldl(+, 0, xs)' to sum a list"
                                .to_string(),
                        );
                    }
                    "sort" | "sorted" => {
                        notes.push(
                            "to sort a list of numbers, use 'sort-nums', e.g. 'xs sort-nums'; \
                             for custom ordering use 'sort-by-num(f, xs)' or 'sort-by(f, xs)'"
                                .to_string(),
                        );
                    }
                    "slice" => {
                        notes.push(
                            "to take the first n elements of a list use 'take', e.g. 'xs take(n)'; \
                             to drop the first n elements use 'drop', e.g. 'xs drop(n)'"
                                .to_string(),
                        );
                    }
                    "split" | "splitOn" | "split_on" => {
                        notes.push(
                            "to split a string, use 'str.split-on', e.g. \
                             'text str.split-on(\" \")' (the separator is a regex pattern)"
                                .to_string(),
                        );
                    }
                    "flatten" | "flat" => {
                        notes.push(
                            "to flatten a list of lists, use 'concat', e.g. 'xs concat'"
                                .to_string(),
                        );
                    }
                    "length" | "len" | "size" => {
                        notes.push(
                            "to count the elements of a list, use 'count', e.g. 'xs count'"
                                .to_string(),
                        );
                    }
                    "contains" | "includes" => {
                        notes.push(
                            "to test if a list contains a value, use 'any', \
                             e.g. 'xs any(42 = _)' to test for element 42"
                                .to_string(),
                        );
                        notes.push(
                            "to test if a string contains a pattern, use 'str.matches?', \
                             e.g. 'text str.matches?(\"pattern\")'"
                                .to_string(),
                        );
                    }
                    "join" => {
                        notes.push(
                            "to join strings with a separator, use 'join-on', \
                             e.g. 'xs join-on(sep)' where sep is the separator string"
                                .to_string(),
                        );
                    }
                    "index" | "get" | "at" => {
                        notes.push(
                            "to index into a list, use 'nth', e.g. 'xs nth(0)' for \
                             the first element, or the '!!' operator: 'xs !! 0'"
                                .to_string(),
                        );
                    }
                    "print" | "println" | "printf" | "puts" | "console" => {
                        notes.push(
                            "eucalypt has no print function; outputs are produced by rendering \
                             the result value — set the RESULT target to what you want to output"
                                .to_string(),
                        );
                    }
                    // Capitalized boolean literals from Python/Ruby/Haskell.
                    // Eucalypt uses lowercase 'true' and 'false'.
                    "True" => {
                        notes.push("note: eucalypt uses lowercase 'true', not 'True'".to_string())
                    }
                    "False" => {
                        notes.push("note: eucalypt uses lowercase 'false', not 'False'".to_string())
                    }
                    // Null/None from Python, Java, JavaScript, Ruby
                    "None" | "NULL" | "Null" | "undefined" | "Undefined" => notes.push(
                        "note: eucalypt uses 'null' for the null/nil value, not 'None'/'NULL'"
                            .to_string(),
                    ),
                    // Common Haskell/ML keywords that don't exist in eucalypt
                    "else" | "then" => {
                        notes.push(
                            "note: eucalypt does not use 'if\u{2026}then\u{2026}else' syntax; \
                             use the 'if' function: 'if(condition, then-value, else-value)'"
                                .to_string(),
                        );
                    }
                    "return" => {
                        notes.push(
                            "note: eucalypt has no 'return' statement; \
                             functions evaluate to their body expression \u{2014} the last \
                             binding value is the function result"
                                .to_string(),
                        );
                    }
                    // Python 'lambda' keyword — eucalypt uses anaphora or named functions
                    "lambda" => {
                        notes.push(
                            "eucalypt has no 'lambda' keyword; use anaphora for anonymous \
                             functions: '(_ * 2)' is a doubling function, or define a named \
                             function with 'double(x): x * 2'"
                                .to_string(),
                        );
                    }
                    // Python/Haskell 'for'/'in' keywords (list comprehension / for-loop)
                    "for" => {
                        notes.push(
                            "eucalypt has no for-loop or list comprehension; use 'map' to \
                             transform lists, e.g. 'xs map(_ * 2)' doubles each element, \
                             or 'xs filter(_ > 0)' to select elements"
                                .to_string(),
                        );
                    }
                    "in" => {
                        notes.push(
                            "eucalypt has no 'in' keyword for list comprehensions; \
                             use 'map' and 'filter' instead: 'xs filter(_ > 0) map(_ * 2)'"
                                .to_string(),
                        );
                    }
                    // Type predicates from Haskell/Clojure/Racket/Ruby.
                    "even?" | "even" | "isEven" | "is_even" => {
                        notes.push(
                            "eucalypt has no built-in 'even?' predicate; \
                             test with the modulo operator: '(_ % 2 = 0)'"
                                .to_string(),
                        );
                        notes.push(
                            "example: 'xs filter(_ % 2 = 0)' to keep even numbers".to_string(),
                        );
                    }
                    "odd?" | "odd" | "isOdd" | "is_odd" => {
                        notes.push(
                            "eucalypt has no built-in 'odd?' predicate; \
                             test with the modulo operator: '(_ % 2 = 1)'"
                                .to_string(),
                        );
                        notes.push(
                            "example: 'xs filter(_ % 2 = 1)' to keep odd numbers".to_string(),
                        );
                    }
                    // Common names for string-to-number conversion from other languages.
                    // In eucalypt the function is simply 'num'.
                    "parse-num" | "parseInt" | "parseFloat" | "parseNumber" | "parse_int"
                    | "parse_float" | "to-num" | "to_num" | "atoi" | "atof" => {
                        notes.push(
                            "to convert a string to a number in eucalypt, use 'num', \
                             e.g. 's num'"
                                .to_string(),
                        );
                    }
                    // Function definition keywords from Python, Ruby, Rust, JavaScript, Haskell.
                    // In eucalypt, functions are declared with a colon: 'square(x): x * x'.
                    "def" | "fn" | "fun" | "func" | "function" | "defn" | "define" => {
                        notes.push(
                            "eucalypt does not use a 'def' or 'fn' keyword for functions; \
                             define a function with 'name(args): body', \
                             e.g. 'square(x): x * x'"
                                .to_string(),
                        );
                    }
                    // String formatting functions from C, Python, Ruby, JavaScript.
                    // Eucalypt uses string interpolation with {name} placeholders.
                    // Note: 'printf' is matched by the print/println arm above.
                    "sprintf" | "format" | "string_format" | "str.format" | "fmt"
                    | "format_string" => {
                        notes.push(
                            "eucalypt has no sprintf/format function; for printf-style \
                             numeric formatting use 'str.fmt', e.g. 'x str.fmt(\"%.2f\")' \
                             for two decimal places"
                                .to_string(),
                        );
                        notes.push(
                            "for string interpolation of named values, use \
                             \"Hello, {name}!\" where 'name' is a binding in scope"
                                .to_string(),
                        );
                    }
                    // 'where' clause from Haskell, OCaml, SQL.
                    // Eucalypt uses inline block bindings instead.
                    "where" => {
                        notes.push(
                            "eucalypt has no 'where' clause; define helper bindings inside \
                             a block: '{ helper: 1  result: helper + 2 }'"
                                .to_string(),
                        );
                        notes.push(
                            "or bind before use at the top level: \
                             'helper: 1  result: helper + 2'"
                                .to_string(),
                        );
                    }
                    // 'let'/'in' from Haskell, Scheme, OCaml, ML.
                    // Eucalypt uses block declarations instead of let-bindings.
                    "let" => {
                        notes.push(
                            "eucalypt has no 'let' expression; use block bindings instead: \
                             '{ x: 1  y: x + 2 }' or top-level declarations 'x: 1  y: x + 2'"
                                .to_string(),
                        );
                    }
                    // CamelCase names for list functions that have kebab-case equivalents.
                    // Haskell, Scala, and JavaScript all use camelCase for these.
                    "takeWhile" | "take_while" => {
                        notes.push(
                            "eucalypt uses kebab-case: 'take-while', e.g. \
                             'xs take-while(_ < 4)'"
                                .to_string(),
                        );
                    }
                    "dropWhile" | "drop_while" => {
                        notes.push(
                            "eucalypt uses kebab-case: 'drop-while', e.g. \
                             'xs drop-while(_ < 4)'"
                                .to_string(),
                        );
                    }
                    "zipWith" | "zip_with" => {
                        notes.push(
                            "eucalypt uses kebab-case: 'zip-with', e.g. \
                             'zip-with(+, xs, ys)' or 'xs zip-with(+, ys)'"
                                .to_string(),
                        );
                    }
                    "groupBy" | "group_by" => {
                        notes.push(
                            "eucalypt uses kebab-case: 'group-by', e.g. \
                             'xs group-by(_ % 2)' to group by remainder"
                                .to_string(),
                        );
                    }
                    "sortBy" | "sort_by" => {
                        notes.push(
                            "eucalypt uses kebab-case: 'sort-by' (for general ordering) or \
                             'sort-by-num' (for numeric keys), e.g. \
                             'xs sort-by-num(.score)'"
                                .to_string(),
                        );
                    }
                    _ => {}
                }
                diag.with_notes(notes)
            }
            _ => diag,
        }
    }
}

/// A linked list approach to context
pub struct Context<'a> {
    /// If this context scope has arisen from a core letrec
    scope: Option<RcExpr>,

    /// Indices corresponding to core let bindings (bound vars)
    var_refs: Vec<Ref>,

    /// What offset we need to count back to cross this scope
    size: usize,

    /// Next context frame.
    ///
    /// Note that this may not be the enclosing, if we are embedded in
    /// the binding of a non-recursive let, that let is skipped as it
    /// does not contribute bindings to our context.
    next: Option<&'a Context<'a>>,
}

impl Context<'_> {
    /// The core expression representing an underlying scope, if not synthetic
    pub fn scope(&self) -> Option<&RcExpr> {
        self.scope.as_ref()
    }

    /// Size (or offset required to traverse this scope)
    pub fn size(&self) -> usize {
        self.size
    }

    /// Is a purely synthetic context, created during compile, rather
    /// than corresponding to an underlying scope in core syntax.
    pub fn is_synthetic(&self) -> bool {
        self.scope().is_none()
    }

    /// Next or error
    pub fn next(&self) -> Result<&Context<'_>, CompileError> {
        match self.next {
            Some(frame) => Ok(frame),
            None => Err(CompileError::BoundVarOverflowsContext),
        }
    }

    pub fn local(&self, bound_var: &BoundVar) -> Result<Rc<StgSyn>, CompileError> {
        Ok(dsl::atom(self.lookup(bound_var)?))
    }

    /// Generate a STG syntax local ref corresponding to the specified
    /// Core bound variable
    pub fn lookup(&self, bound_var: &BoundVar) -> Result<Ref, CompileError> {
        let BoundVar { scope, binder, .. } = bound_var;

        if self.is_synthetic() {
            self.next()?.lookup(bound_var).map(|r| r.bump(self.size))
        } else if *scope == 0 {
            Ok(self.var_refs[*binder as usize].clone())
        } else {
            let adjusted_var = BoundVar {
                scope: scope
                    .checked_sub(1)
                    .ok_or(CompileError::BoundVarOverflowsContext)?,
                binder: bound_var.binder,
                name: None,
            };
            self.next()?
                .lookup(&adjusted_var)
                .map(|r| r.bump(self.size))
        }
    }
}

/// Compiler for translating core to STG
///
/// References intrinsics to support inlining
pub struct Compiler<'rt> {
    /// Whether to generate source annotations in let bindings
    generate_annotations: bool,
    /// Whether to wrap a render
    render_type: RenderType,
    /// Suppress updates by generating values instead of thunks
    suppress_updates: bool,
    /// Turn inlining off
    suppress_inlining: bool,
    /// Turn optimisations off
    suppress_optimiser: bool,
    /// Intrinsics
    intrinsics: Vec<&'rt dyn StgIntrinsic>,
    /// Per-intrinsic demand signatures seeded from the demand analysis.
    ///
    /// Maps intrinsic name → per-argument demand vector. Used in
    /// `compile_bif_application()` to set per-argument demands instead
    /// of consulting `Intrinsic::strict_args()`.
    intrinsic_demand_sigs: HashMap<String, Vec<crate::core::demand::Demand>>,
    /// Prelude binding name → slot index (relative to `INTRINSIC_COUNT`).
    ///
    /// When set, `Var::Free(name)` references to prelude names are compiled to
    /// `Ref::G(intrinsic_count + slot)` rather than raising `CompileError::FreeVar`.
    /// This is how user code compiled independently of the prelude references
    /// the pre-loaded prelude global slots.
    prelude_globals: Option<HashMap<String, usize>>,
    /// Per-user-function demand signatures from the analysis pass.
    ///
    /// Maps binding name → per-argument demand vector. Used when
    /// compiling `App(user_function, args)` to set per-argument demands.
    user_demand_sigs: crate::core::analyse_demand::NamedSignatureTable,
}

/// An item which can be converted to STG syntax once the context in known
///
/// Each Proto form should implement at least one of `take_lambda_form`
/// and `take_syntax`
pub trait ProtoSyntax {
    /// The demand annotation for this binding.
    ///
    /// Used by `take_lambda_form` to decide whether to emit a `Value` or
    /// a `Thunk`.  Defaults to `Demand::default()` (all `Unknown`), which
    /// is conservative — the compiler emits a `Thunk` unless overridden.
    fn demand(&self) -> Demand {
        Demand::default()
    }

    /// Convert into lambda form (destroying the proto form)
    ///
    /// Consults the binding's `Demand` annotation and the compiled syntax's
    /// `is_whnf()` predicate at a single decision point.  The global
    /// `suppress_updates` flag is an escape hatch for debugging.
    fn take_lambda_form(
        &mut self,
        compiler: &Compiler,
        context: &Context,
    ) -> Result<LambdaForm, CompileError> {
        let syntax = self.take_syntax(compiler, context)?;
        // Unify the Value-vs-Thunk decision behind one place:
        //   • demand.whnf — body is already in WHNF (set after compiling)
        //   • demand.cardinality == AtMostOnce — single-use, no Update benefit
        //   • syntax.is_whnf() — compiled form is a known WHNF value
        //   • suppress_updates — global escape hatch
        let demand = self.demand();
        let skip_update = demand.skip_update() || syntax.is_whnf() || compiler.suppress_updates;
        if skip_update {
            Ok(dsl::value(syntax))
        } else {
            Ok(dsl::thunk(syntax))
        }
    }

    /// Convert into STG syntax (destroying the proto form)
    ///
    /// Default allocates the `take_lambda_form` result in a synthetic
    /// letrec and references the binding.
    fn take_syntax(
        &mut self,
        compiler: &Compiler,
        context: &Context,
    ) -> Result<Rc<StgSyn>, CompileError> {
        // we'll use a plain let so the LambdaForm does not need to
        // refer to the let's context
        let lambda_form = self.take_lambda_form(compiler, context)?;

        // the body would need to take account of the let context but
        // we can trivially hardcode the reference
        let body = dsl::local(0);

        Ok(dsl::let_(vec![lambda_form], body))
    }
}

/// An item which can be converted to a StgSyn once a context is available
pub trait ProtoReference {
    /// Convert into STG reference (destroying the proto form)
    fn take_reference(&mut self, context: &Context) -> Result<Ref, CompileError>;
}

/// We don't know the size of the environment as we add bindings to a
/// let so we track them in order to generate the entire let once we
/// are complete.
#[derive(Default)]
pub struct LetBinder<'a> {
    /// If this context scope has arisen from
    scope: Option<RcExpr>,
    /// Whether this should compile to LETREC as opposed to LET
    recursive: bool,
    /// Bindings in construction
    bindings: Vec<Box<dyn ProtoSyntax>>,
    /// If this corresponds to a Core let (i.e. has a scope), track
    /// the indices in our bindings which correspond to the scope
    /// bindings (as opposed to extra supporting bindings, generated)
    /// or potentially if bindings have been optimised away, the
    /// globals that replace them
    var_refs: Vec<Ref>,
    /// Body in construction
    body: Option<Box<dyn ProtoSyntax>>,
    /// When frozen, the number of bindings added
    size: Option<usize>,
    /// Next
    context: Option<&'a Context<'a>>,
    /// Indices of bindings that are strict and non-recursive, eligible
    /// for eager evaluation via Seq in non-recursive lets.
    strict_indices: Vec<usize>,
}

impl<'a> LetBinder<'a> {
    /// A letrec scope for the root context
    pub fn root() -> Self {
        LetBinder {
            scope: None,
            recursive: true,
            bindings: vec![],
            var_refs: vec![],
            body: None,
            size: None,
            context: None,
            strict_indices: vec![],
        }
    }

    /// A let scope to catch any required intermediate bindings
    /// without a corresponding Core scope
    pub fn synthetic_let(context: &'a Context) -> Self {
        LetBinder {
            scope: None,
            recursive: false,
            bindings: vec![],
            var_refs: vec![],
            body: None,
            size: None,
            context: Some(context),
            strict_indices: vec![],
        }
    }

    /// A recursive let binder to catch any required intermediate
    /// bindings without a corresponding Core scope
    pub fn synthetic_letrec(context: &'a Context) -> Self {
        LetBinder {
            scope: None,
            recursive: true,
            bindings: vec![],
            var_refs: vec![],
            body: None,
            size: None,
            context: Some(context),
            strict_indices: vec![],
        }
    }

    /// A new letrec binder corresponding to a scope in the Core syntax
    pub fn for_scope(expr: RcExpr, context: &'a Context) -> Self {
        LetBinder {
            scope: Some(expr),
            recursive: true,
            bindings: vec![],
            var_refs: vec![],
            body: None,
            size: None,
            context: Some(context),
            strict_indices: vec![],
        }
    }

    /// If we already know what reference a bound var index
    /// corresponds to, we can generate a ref right away and optimise
    /// away a binding.
    pub fn running_ref(&self, index: usize) -> Option<Ref> {
        if self.recursive {
            self.var_refs.get(index).cloned()
        } else {
            None
        }
    }

    /// Stop accepting bindings and crystalise size
    pub fn freeze(&mut self) {
        self.size = Some(self.bindings.len())
    }

    /// Ensure that this binder will now generate a LETREC.
    ///
    /// If we're adding inter-referring bindings we need to ensure
    /// that however the binder was created, it will generate a
    /// LETREC.
    pub fn ensure_recursive(&mut self) {
        self.recursive = true;
    }

    /// Once a let binder has been frozen, so the size of the bindings
    /// is known and it has a body, it can be converted into an STG LetRec
    pub fn into_stg(mut self, compiler: &Compiler) -> Result<Rc<StgSyn>, CompileError> {
        let recursive_let = self.recursive;

        if self.size.is_none() {
            panic!("attempt to realise an unfrozen let binder");
        }

        if self.body.is_none() {
            panic!("attempt to realise let binder with no body")
        }

        let body_context = Context {
            scope: self.scope,
            var_refs: self.var_refs,
            size: self
                .size
                .expect("LetBinder must be frozen before realisation"),
            next: self.context,
        };

        let binding_context = if recursive_let {
            &body_context
        } else {
            self.context
                .as_ref()
                .expect("non-recursive let must have enclosing context")
        };

        let bindings: Vec<LambdaForm> = self
            .bindings
            .drain(0..)
            .map(|mut b| b.take_lambda_form(compiler, binding_context))
            .collect::<Result<Vec<LambdaForm>, CompileError>>()?;

        let mut proto_body = self
            .body
            .take()
            .expect("LetBinder must have body before realisation");
        let body = proto_body.take_syntax(compiler, &body_context)?;

        if bindings.is_empty() {
            Ok(body)
        } else {
            // For strict non-recursive Thunk bindings, wrap the body
            // with Seq forms that force each thunk eagerly at definition
            // time.  Seq is force-and-discard: it evaluates the scrutinee
            // to WHNF (triggering the thunk's Update continuation for
            // memoisation) then enters the body in the SAME environment.
            // No new scope is created, so no de Bruijn index shifting is
            // needed.
            //
            // Structure:
            //   LET/LETREC([bindings...],
            //     SEQ(ATOM(L(i0)),          -- force thunk i0
            //       SEQ(ATOM(L(i1)),        -- force thunk i1
            //         ... body ...)))
            let strict_thunk_indices: Vec<usize> = self
                .strict_indices
                .iter()
                .copied()
                .filter(|&idx| matches!(bindings[idx], LambdaForm::Thunk { .. }))
                .collect();

            let body = if strict_thunk_indices.is_empty() {
                body
            } else {
                // Wrap the body with Seq forms that force each strict thunk.
                // Seq is force-and-discard: it evaluates the scrutinee to WHNF
                // (triggering the thunk's Update continuation for memoisation)
                // then enters the body in the SAME environment — no new scope.
                let mut result = body;
                // Wrap inside-out (last strict index is outermost)
                for &idx in strict_thunk_indices.iter().rev() {
                    let scrutinee = dsl::local(idx);
                    result = dsl::seq(scrutinee, result);
                }
                result
            };

            if recursive_let {
                Ok(Rc::new(StgSyn::LetRec { bindings, body }))
            } else {
                Ok(Rc::new(StgSyn::Let { bindings, body }))
            }
        }
    }

    /// Add a deferred binding which will be realised when the number
    /// of bindings is known. Free vars (i.e. refs into environments
    /// below the nascent letrec) will need accessing with indexes
    /// which include the count of bound vars.
    pub fn add_deferred(&mut self, form: Box<dyn ProtoSyntax>) -> Result<Ref, CompileError> {
        if self.size.is_some() {
            panic!("binding is frozen")
        } else {
            self.bindings.push(form);
            Ok(dsl::lref(self.bindings.len() - 1))
        }
    }

    /// Add a binding to syntax which is already determined
    /// (regardless of unknown context)
    pub fn add(&mut self, syntax: Rc<StgSyn>) -> Result<Ref, CompileError> {
        self.add_deferred(Box::new(Holder::new(syntax)))
    }

    /// Mark the binding at the given index as strict for eager
    /// evaluation via Seq in non-recursive lets.
    pub fn mark_strict(&mut self, index: usize) {
        if !self.strict_indices.contains(&index) {
            self.strict_indices.push(index);
        }
    }

    /// Add a ref as an equivalent to an underlying core let binding
    pub fn add_var_index(&mut self, reference: Ref) {
        self.var_refs.push(reference);
    }

    /// Add a fn to calculate the body once the context is known
    pub fn set_body(&mut self, body: Box<dyn ProtoSyntax>) -> Result<(), CompileError> {
        self.body = Some(body);
        Ok(())
    }
}

struct ProtoLet {
    expr: RcExpr,
    /// Demand from the enclosing binding — propagated so that single-use
    /// bindings whose values are nested let expressions are compiled as
    /// `Value` rather than `Thunk`.
    demand: Demand,
}

impl ProtoLet {
    pub fn new(expr: RcExpr) -> Self {
        assert!(matches!(&*expr.inner, Expr::Let(_, _, _)));
        ProtoLet {
            expr,
            demand: Demand::default(),
        }
    }

    /// Create a `ProtoLet` with an explicit demand annotation.
    pub fn with_demand(expr: RcExpr, demand: Demand) -> Self {
        assert!(matches!(&*expr.inner, Expr::Let(_, _, _)));
        ProtoLet { expr, demand }
    }

    /// Reference the scope inside the let expression
    fn scope(&self) -> &LetScope<RcExpr> {
        if let Expr::Let(_, scope, _) = &*self.expr.inner {
            scope
        } else {
            unreachable!()
        }
    }
}

impl ProtoSyntax for ProtoLet {
    fn demand(&self) -> Demand {
        self.demand
    }

    fn take_syntax(
        &mut self,
        compiler: &Compiler,
        context: &Context,
    ) -> Result<Rc<StgSyn>, CompileError> {
        use crate::core::demand::Strictness;

        let scope = self.scope();
        let mut binder = LetBinder::for_scope(self.expr.clone(), context);
        for b in scope.pattern.iter() {
            let annotation = b.expr.smid();
            let index =
                compiler.compile_binding(&mut binder, b.expr.clone(), annotation, b.demand)?;
            // Mark strict non-recursive bindings for eager Seq evaluation
            if b.demand.strictness == Strictness::Strict && !b.demand.recursive {
                if let Ref::L(n) = index {
                    binder.mark_strict(n);
                }
            }
            binder.add_var_index(index);
        }

        let body = compiler.compile_body(&mut binder, scope.body.clone())?;
        binder.set_body(body)?;
        binder.freeze();

        binder.into_stg(compiler)
    }
}

