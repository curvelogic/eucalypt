/// A function application using local let bindings
pub struct ProtoAppGroup {
    f: RcExpr,
    args: Vec<RcExpr>,
    demand: Demand,
    smid: Smid,
}

impl ProtoAppGroup {
    pub fn new(f: RcExpr, args: Vec<RcExpr>, demand: Demand, smid: Smid) -> Self {
        Self {
            f,
            args,
            demand,
            smid,
        }
    }
}

impl ProtoSyntax for ProtoAppGroup {
    fn demand(&self) -> Demand {
        self.demand
    }

    fn take_syntax(
        &mut self,
        compiler: &Compiler,
        context: &Context,
    ) -> Result<Rc<StgSyn>, CompileError> {
        use crate::core::demand::Strictness;
        let mut intrinsic_index = None;
        let mut intrinsic_name: Option<&str> = None;
        let mut callee_name: Option<&str> = None;
        let mut local_binder = LetBinder::synthetic_let(context);

        // Find a reference for the function
        let f_index: Box<dyn ProtoReference> = match &*self.f.inner {
            Expr::Var(s, v) => match v {
                Var::Bound(bv) => {
                    // Preserve the binding name for user demand signature lookup.
                    callee_name = bv.name.as_deref();
                    Box::new(ProtoVar::new(bv.clone()))
                }
                Var::Free(name) => {
                    callee_name = Some(name.as_str());
                    Box::new(ProtoRef::new(compiler.resolve_free_var(*s, name)?))
                }
            },
            Expr::Intrinsic(_, bif) => {
                intrinsic_index = intrinsics::index(bif);
                let n =
                    intrinsic_index.ok_or_else(|| CompileError::UnknownIntrinsic(bif.clone()))?;
                intrinsic_name = Some(intrinsics::intrinsic(n).name());
                Box::new(ProtoRef::new(gref(n)))
            }
            _ => Box::new(ProtoRef::new(compiler.compile_binding(
                &mut local_binder,
                self.f.clone(),
                self.smid,
                Demand::default(),
            )?)),
        };

        // Look up per-argument demands.  Try intrinsic signatures first,
        // then fall back to user function signatures from the demand analysis.
        let arg_demands: Option<&[crate::core::demand::Demand]> = intrinsic_name
            .and_then(|name| compiler.intrinsic_demand_sigs.get(name))
            .map(|v| v.as_slice())
            .or_else(|| {
                callee_name
                    .and_then(|name| compiler.user_demand_sigs.get(name))
                    .map(|v| v.as_slice())
            });

        let mut arg_indexes: Vec<Box<dyn ProtoReference>> = vec![];
        for (i, arg) in self.args.iter().enumerate() {
            match &*arg.inner {
                Expr::Var(s, v) => match v {
                    Var::Bound(bv) => arg_indexes.push(Box::new(ProtoVar::new(bv.clone()))),
                    Var::Free(name) => arg_indexes.push(Box::new(ProtoRef::new(
                        compiler.resolve_free_var(*s, name)?,
                    ))),
                },
                Expr::Intrinsic(_, bif) => {
                    let global_index = intrinsics::index(bif)
                        .ok_or_else(|| CompileError::UnknownIntrinsic(bif.clone()))?;
                    arg_indexes.push(Box::new(ProtoRef::new(gref(global_index))))
                }
                _ => {
                    // Use the per-argument demand from the intrinsic signature table.
                    // Falls back to Demand::default() for unknown intrinsics or
                    // out-of-range argument indices.
                    let arg_demand = arg_demands
                        .and_then(|sigs| sigs.get(i))
                        .copied()
                        .unwrap_or_default();
                    let index = compiler.compile_binding(
                        &mut local_binder,
                        arg.clone(),
                        self.smid,
                        arg_demand,
                    )?;
                    // Mark strict non-recursive arg bindings for eager Seq evaluation
                    if arg_demand.strictness == Strictness::Strict && !arg_demand.recursive {
                        if let Ref::L(n) = index {
                            local_binder.mark_strict(n);
                        }
                    }
                    arg_indexes.push(Box::new(ProtoRef::new(index)));
                }
            }
        }

        // If it's an intrinsic, check whether we should inline the
        // wrapper
        match intrinsic_index.and_then(|index| compiler.intrinsics.get(index)) {
            Some(bif)
                if !compiler.suppress_inlining
                    && bif.inlinable()
                    && bif.info().arity() == arg_indexes.len() =>
            {
                let inline_body = bif.wrapper(Smid::default()).body().clone();
                local_binder.set_body(Box::new(ProtoInline::new(arg_indexes, inline_body)))?;
            }
            _ => {
                local_binder.set_body(ProtoApp::boxed(f_index, arg_indexes, self.demand))?;
            }
        };

        local_binder.freeze();
        let stg = local_binder.into_stg(compiler)?;

        // Wrap function calls with a source annotation so that runtime errors
        // (TypeMismatch, NoBranchForDataTag, etc.) carry the user's call site.
        // Applied whenever source tracking is enabled and the call site has a
        // valid Smid. The IO spec block navigator (block_list_inner) handles
        // Ann nodes transparently, so this is safe for all call sites.
        let stg = if compiler.generate_annotations() && self.smid.is_valid() {
            dsl::ann(self.smid, stg)
        } else {
            stg
        };

        Ok(stg)
    }
}

/// A function application that resolves all locals when context is available
pub struct ProtoApp {
    f: Box<dyn ProtoReference>,
    args: Vec<Box<dyn ProtoReference>>,
    demand: Demand,
}

impl ProtoApp {
    pub fn boxed(
        f: Box<dyn ProtoReference>,
        args: Vec<Box<dyn ProtoReference>>,
        demand: Demand,
    ) -> Box<dyn ProtoSyntax> {
        Box::new(ProtoApp { f, args, demand })
    }
}

impl ProtoSyntax for ProtoApp {
    fn demand(&self) -> Demand {
        self.demand
    }

    fn take_syntax(
        &mut self,
        _compiler: &Compiler,
        context: &Context,
    ) -> Result<Rc<StgSyn>, CompileError> {
        let callable = self.f.take_reference(context)?;
        let args = self
            .args
            .drain(0..)
            .map(|mut a| a.take_reference(context))
            .collect::<Result<Vec<Ref>, CompileError>>()?;
        Ok(Rc::new(StgSyn::App { callable, args }))
    }
}

/// Extract reference to bound var
pub fn extract_bound_var<'a>(smid: &'a Smid, var: &'a Var) -> Result<&'a BoundVar, CompileError> {
    match var {
        Var::Bound(bound_var) => Ok(bound_var),
        Var::Free(name) => Err(CompileError::FreeVar(*smid, name.clone())),
    }
}

/// Compile literal into a consistent boxed representation
pub fn compile_boxed_literal(prim: &Primitive) -> Rc<StgSyn> {
    match prim {
        Primitive::Str(s) => dsl::box_str(s),
        Primitive::Sym(s) => dsl::box_sym(s),
        Primitive::Num(n) => dsl::box_num(n.clone()),
        Primitive::Bool(b) => dsl::bool_(*b),
        Primitive::Null => dsl::unit(),
        Primitive::TypeData(s) => dsl::box_type_data(s),
    }
}

pub struct ProtoLambda {
    expr: RcExpr,
    annotation: Smid,
}

impl ProtoLambda {
    pub fn new(expr: RcExpr, annotation: Smid) -> Self {
        assert!(matches!(&*expr.inner, Expr::Lam(_, _, _)));
        ProtoLambda { expr, annotation }
    }

    /// Reference the scope inside the lambda expression
    fn scope(&self) -> &LamScope<RcExpr> {
        if let Expr::Lam(_, _, scope) = &*self.expr.inner {
            scope
        } else {
            unreachable!()
        }
    }
}

impl ProtoSyntax for ProtoLambda {
    /// Compiling a lambda introduced a context for the bound
    /// variables and a synthetic let binder for the body in case it
    /// is required.
    fn take_lambda_form(
        &mut self,
        compiler: &Compiler,
        context: &Context,
    ) -> Result<LambdaForm, CompileError> {
        let scope = self.scope();
        let args = scope.pattern.len();

        let lambda_context = Context {
            scope: Some(self.expr.clone()),
            var_refs: (0..args).map(Ref::L).collect(),
            size: args,
            next: Some(context),
        };

        let mut binder = LetBinder::synthetic_let(&lambda_context);

        let body = compiler.compile_body(&mut binder, scope.body.clone())?;
        binder.set_body(body)?;
        binder.freeze();
        let mut body = binder.into_stg(compiler)?;

        if compiler.generate_annotations() {
            body = dsl::ann(self.annotation, body);
        }

        Ok(dsl::annotated_lambda(
            args.try_into().or(Err(CompileError::MaxLambdaArgs))?,
            body,
            self.annotation,
        ))
    }
}

pub struct ProtoInline {
    args: Vec<Box<dyn ProtoReference>>,
    body: Rc<StgSyn>,
}

impl ProtoInline {
    pub fn new(args: Vec<Box<dyn ProtoReference>>, body: Rc<StgSyn>) -> Self {
        ProtoInline { args, body }
    }
}

impl ProtoSyntax for ProtoInline {
    /// Inline an application by creating a let binding to hold the
    /// args then substituting the lambda body.
    ///
    /// We need an optimisation pass to eliminate needless lets as
    /// they're only refs to elsewhere
    fn take_syntax(
        &mut self,
        _compiler: &Compiler,
        context: &Context,
    ) -> Result<Rc<StgSyn>, CompileError> {
        let body = self.body.clone();

        let refs = self
            .args
            .drain(..)
            .map(|mut pr| pr.take_reference(context))
            .collect::<Result<Vec<Ref>, CompileError>>()?;

        let bindings = refs.into_iter().map(|r| dsl::value(dsl::atom(r))).collect();

        Ok(Rc::new(StgSyn::Let { bindings, body }))
    }
}

impl<'rt> Compiler<'rt> {
    /// Temporary pending a builder pattern....
    pub fn new(
        generate_annotations: bool,
        render_type: RenderType,
        suppress_updates: bool,
        suppress_inlining: bool,
        suppress_optimiser: bool,
        intrinsics: Vec<&'rt dyn StgIntrinsic>,
        prelude_globals: Option<HashMap<String, usize>>,
    ) -> Self {
        let intrinsic_demand_sigs = crate::core::analyse_demand::build_intrinsic_signatures();
        Compiler {
            generate_annotations,
            render_type,
            suppress_updates,
            suppress_inlining,
            suppress_optimiser,
            intrinsics,
            intrinsic_demand_sigs,
            prelude_globals,
            user_demand_sigs: Default::default(),
        }
    }

    /// Set the user function demand signature table.
    pub fn with_user_demand_sigs(
        mut self,
        sigs: crate::core::analyse_demand::NamedSignatureTable,
    ) -> Self {
        self.user_demand_sigs = sigs;
        self
    }

    /// Resolve a free variable name to a global slot reference.
    ///
    /// Called when `extract_bound_var` would fail with `CompileError::FreeVar`.
    /// If `prelude_globals` contains the name, returns `Ref::G(intrinsic_count + slot)`.
    /// Otherwise returns the `FreeVar` error so the caller can propagate it.
    pub fn resolve_free_var(&self, smid: Smid, name: &str) -> Result<Ref, CompileError> {
        if let Some(map) = &self.prelude_globals {
            if let Some(&slot) = map.get(name) {
                let base = intrinsics::catalogue().len();
                return Ok(gref(base + slot));
            }
        }
        Err(CompileError::FreeVar(smid, name.to_string()))
    }

    /// Whether to generate source annotations
    pub fn generate_annotations(&self) -> bool {
        self.generate_annotations
    }

    /// Compile a core expression into STG syntax
    pub fn compile(&self, expr: RcExpr) -> Result<Rc<StgSyn>, CompileError> {
        let mut binder = LetBinder::root();
        match self.render_type {
            RenderType::Headless => {
                let body = self.compile_body(&mut binder, expr)?;
                binder.set_body(body)?;
            }
            RenderType::RenderDoc => {
                let index = self.compile_binding(
                    &mut binder,
                    expr.clone(),
                    expr.smid(),
                    Demand::default(),
                )?;
                binder.set_body(Box::new(Holder::new(RenderDoc.global(index))))?;
            }
            RenderType::RenderFragment => {
                let index = self.compile_binding(
                    &mut binder,
                    expr.clone(),
                    expr.smid(),
                    Demand::default(),
                )?;
                binder.set_body(Box::new(Holder::new(Render.global(index))))?;
            }
        }
        binder.freeze();
        let compiled = binder.into_stg(self);

        if self.suppress_optimiser {
            compiled
        } else {
            compiled.map(|c| optimiser::AllocationPruner::default().apply(c))
        }
    }

    /// Compile a let body or standalone expression
    pub fn compile_body(
        &self,
        binder: &mut LetBinder,
        expr: RcExpr,
    ) -> Result<Box<dyn ProtoSyntax>, CompileError> {
        match &*expr.inner {
            Expr::Let(_, _, _) => Ok(Box::new(ProtoLet::new(expr))),
            Expr::Var(s, v) => match v {
                Var::Bound(bv) => Ok(Box::new(ProtoVar::new(bv.clone()))),
                Var::Free(name) => Ok(Box::new(ProtoRef::new(self.resolve_free_var(*s, name)?))),
            },
            Expr::App(s, f, args) => {
                // Demand::at_most_once() — a let body is evaluated exactly once,
                // so no Update frame is needed. (Bindings use Unknown because
                // they may be shared.)
                let proto_app =
                    self.compile_application(binder, *s, f, args, Demand::at_most_once())?;
                Ok(proto_app)
            }
            Expr::Literal(_, n) => Ok(Box::new(Holder::new(compile_boxed_literal(n)))),
            Expr::Lam(s, _, _) => Ok(Box::new(self.compile_lambda(&expr, *s)?)),
            Expr::List(s, xs) => {
                let list = self.compile_list_body(binder, *s, xs)?;
                Ok(Box::new(list))
            }
            Expr::Block(s, map) => {
                let block = self.compile_block(binder, *s, map)?;
                Ok(Box::new(block))
            }
            Expr::Intrinsic(_, name) => {
                let index = intrinsics::index(name)
                    .ok_or_else(|| CompileError::UnknownIntrinsic(name.clone()))?;
                Ok(Box::new(Holder::new(dsl::atom(gref(index)))))
            }
            Expr::Lookup(s, obj, k, d) => {
                let lookup = self.compile_lookup(binder, obj, k, d, *s)?;
                Ok(Box::new(lookup))
            }
            Expr::Meta(s, body, meta) => {
                let m = self.compile_binding(binder, meta.clone(), *s, Demand::default())?;
                let b = self.compile_binding(binder, body.clone(), *s, Demand::default())?;
                Ok(Box::new(Holder::new(dsl::with_meta(m, b))))
            }
            Expr::Operator(_, _, _, body) => self.compile_body(binder, body.clone()),
            x => Err(CompileError::UnexpectedExpression(x.smid())),
        }
    }

    /// Compile an expression as a binding, returning index in the binder
    /// that the final expression is located at.
    ///
    /// `demand` is the demand annotation for the resulting binding — it flows
    /// through to `ProtoAppGroup` / `ProtoApp` which use it in `take_lambda_form`
    /// to decide Value vs. Thunk.
    pub fn compile_binding(
        &self,
        binder: &mut LetBinder,
        expr: RcExpr,
        annotation: Smid,
        demand: Demand,
    ) -> Result<Ref, CompileError> {
        match &*expr.inner {
            Expr::Var(s, v) => match v {
                Var::Bound(bv) => {
                    let bound_var = bv.clone();
                    if bound_var.scope == 0 {
                        if let Some(r) = binder.running_ref(bound_var.binder as usize) {
                            Ok(r)
                        } else {
                            binder.add_deferred(Box::new(ProtoVar::new(bound_var)))
                        }
                    } else {
                        binder.add_deferred(Box::new(ProtoVar::new(bound_var)))
                    }
                }
                Var::Free(name) => self.resolve_free_var(*s, name),
            },
            Expr::Let(_, _, _) => {
                binder.add_deferred(Box::new(ProtoLet::with_demand(expr, demand)))
            }
            Expr::Lam(_, _, _) => {
                binder.add_deferred(Box::new(self.compile_lambda(&expr, annotation)?))
            }
            Expr::App(s, f, args) => {
                let proto_app = self.compile_application(binder, *s, f, args, demand)?;
                binder.add_deferred(proto_app)
            }
            Expr::Literal(_, n) => binder.add(compile_boxed_literal(n)),
            Expr::List(s, xs) => self.compile_list_binding(binder, *s, xs),
            Expr::Block(s, map) => {
                let block = self.compile_block(binder, *s, map)?;
                binder.add_deferred(Box::new(block))
            }
            Expr::Intrinsic(_, name) => {
                let index = intrinsics::index(name)
                    .ok_or_else(|| CompileError::UnknownIntrinsic(name.clone()))?;
                Ok(dsl::gref(index))
            }
            Expr::Lookup(s, obj, k, d) => {
                let lookup = self.compile_lookup(binder, obj, k, d, *s)?;
                binder.add_deferred(Box::new(lookup))
            }
            Expr::Name(s, _) => Err(CompileError::UnexpectedExpression(*s)),
            Expr::Meta(s, body, meta) => {
                let m = self.compile_binding(binder, meta.clone(), *s, Demand::default())?;
                let b = self.compile_binding(binder, body.clone(), *s, Demand::default())?;
                binder.add(dsl::with_meta(m, b))
            }
            Expr::ArgTuple(s, _) => Err(CompileError::BadArgTupleExpression(*s)),
            Expr::Soup(s, _, _) => Err(CompileError::BadSoupExpression(*s)),
            Expr::Operator(s, _, _, body) => {
                self.compile_binding(binder, body.clone(), *s, Demand::default())
            }
            _ => Err(CompileError::UnexpectedExpression(expr.smid())),
        }
    }

    /// Compile a lookup (with or without default) to a LOOKUPOR call
    pub fn compile_lookup(
        &self,
        binder: &mut LetBinder,
        obj: &RcExpr,
        key: &str,
        dft: &Option<RcExpr>,
        annotation: Smid,
    ) -> Result<Holder, CompileError> {
        binder.ensure_recursive();

        let obj = self.compile_binding(binder, obj.clone(), obj.smid(), Demand::default())?;
        let dft = match dft {
            // Dynamic generalised lookups (from dynamise) may have
            // fallback defaults referencing outer-scope variables
            // that are free at compile time. If the lookup succeeds
            // the default is unused; if it fails we panic anyway.
            Some(expr) => {
                match self.compile_binding(binder, expr.clone(), annotation, Demand::default()) {
                    Ok(expr) => Ok(expr),
                    Err(CompileError::FreeVar(..)) => {
                        let ann = if self.generate_annotations() {
                            annotation
                        } else {
                            Smid::default()
                        };
                        binder.add(lookup_fail(key, obj.clone(), ann))
                    }
                    Err(e) => Err(e),
                }
            }
            None => {
                let ann = if self.generate_annotations() {
                    annotation
                } else {
                    Smid::default()
                };
                binder.add(lookup_fail(key, obj.clone(), ann))
            }
        }?;
        let lookup_stg = LookupOr(NativeVariant::Unboxed).global(dsl::sym(key), dft, obj);
        // Wrap with a source annotation so that lookup type errors (e.g.
        // dot notation on a non-block) carry the user's call-site location.
        let stg = if self.generate_annotations() && annotation.is_valid() {
            dsl::ann(annotation, lookup_stg)
        } else {
            lookup_stg
        };
        Ok(Holder::new(stg))
    }

    /// Compile a lambda to a lambda form
    pub fn compile_lambda(
        &self,
        expr: &RcExpr,
        annotation: Smid,
    ) -> Result<ProtoLambda, CompileError> {
        Ok(ProtoLambda::new(expr.clone(), annotation))
    }

    /// Compile a list into a chain of cons cells in the environment
    /// together with the compiled contents
    pub fn compile_list_body(
        &self,
        binder: &mut LetBinder,
        smid: Smid,
        members: &[RcExpr],
    ) -> Result<Holder, CompileError> {
        binder.ensure_recursive();

        let mut last_cons = None;

        for item in members.iter().rev() {
            let last_index = match last_cons {
                Some(data) => binder.add(data)?,
                None => KEmptyList.gref(),
            };
            let item_index = self.compile_binding(binder, item.clone(), smid, Demand::default())?;
            last_cons = Some(dsl::cons(item_index, last_index));
        }

        match last_cons {
            Some(data) => Ok(Holder::new(data)),
            None => Ok(Holder::new(KEmptyList.global())),
        }
    }

    /// Compile a list into a chain of cons cells in the environment
    /// together with the compiled contents
    pub fn compile_list_binding(
        &self,
        binder: &mut LetBinder,
        smid: Smid,
        members: &[RcExpr],
    ) -> Result<Ref, CompileError> {
        binder.ensure_recursive();

        let mut last_cons = None;

        for item in members.iter().rev() {
            let last_index = match last_cons {
                Some(data) => binder.add(data)?,
                None => KEmptyList.gref(),
            };
            let item_index = self.compile_binding(binder, item.clone(), smid, Demand::default())?;
            last_cons = Some(dsl::cons(item_index, last_index));
        }

        match last_cons {
            Some(data) => binder.add(data),
            None => Ok(KEmptyList.gref()),
        }
    }

    /// Compile a block
    pub fn compile_block(
        &self,
        binder: &mut LetBinder,
        smid: Smid,
        block_map: &BlockMap<RcExpr>,
    ) -> Result<Holder, CompileError> {
        binder.ensure_recursive();

        let mut index = KEmptyList.gref();
        for (k, v) in block_map.iter().rev() {
            let v_index = self.compile_binding(binder, v.clone(), smid, Demand::default())?;
            let kv_index = binder.add(dsl::pair(k, v_index))?;
            index = binder.add(dsl::cons(kv_index, index))?;
        }
        Ok(Holder::new(dsl::block(index)))
    }

    /// Compile a function application
    ///
    /// This may create a let binding for temporaries in function or
    /// argument position.  `demand` is the demand annotation for the
    /// result — carried through to `ProtoAppGroup` and consulted by
    /// `take_lambda_form` at the single Value-vs-Thunk decision point.
    pub fn compile_application(
        &self,
        _binder: &mut LetBinder,
        smid: Smid,
        f: &RcExpr,
        args: &[RcExpr],
        demand: Demand,
    ) -> Result<Box<dyn ProtoSyntax>, CompileError> {
        Ok(Box::new(ProtoAppGroup::new(
            f.clone(),
            args.to_vec(),
            demand,
            smid,
        )))
    }
}
