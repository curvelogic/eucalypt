#!/usr/bin/env python3
"""Generate tests/harness/bench/020_lookup_curve.eu.

Freezes the W3 lookup-evidence spike (docs/superpowers/reports/
2026-07-12-w3-lookup-evidence-spike.md, section 2a) as a committed bench:
static-literal blocks of N=40 and N=250 keys hit by saturated dynamic
`lookup` (16 keys/iteration x M iterations). The bytecode engine takes the
interpreted linear find loop (index gated off, machine.rs:3463); HeapSyn
builds and reuses a key index once (>=16-key threshold). This is the one
synthetic worst case where bc is dramatically slower -- a regression
tripwire, not a real-workload win (see the report's TL;DR).

Deterministic: RESULT checks both round totals against baked-in sums.
Regenerate with:  python3 tests/harness/bench/gen/gen_020_lookup_curve.py
"""

import os

M = 130  # rounds; sized so bc N=250 sits ~2.5s (review C class-E ~1.4s budget). See PROTOCOL exception note: forcing the indexed hs engine >1s would push bc to ~15s.
SAMPLE = 16  # dynamic lookups per iteration (matches the spike harness)


def spread_indices(n, k):
    """k indices spread evenly across 0..n-1 (average find depth ~n/2)."""
    return [round(i * (n - 1) / (k - 1)) for i in range(k)]


def block_literal(n):
    lines = []
    row = []
    for i in range(n):
        row.append(f"k{i:03d}: {i}")
        if len(row) == 10:
            lines.append("  " + "  ".join(row))
            row = []
    if row:
        lines.append("  " + "  ".join(row))
    return "{\n" + "\n".join(lines) + "\n}"


def key_list(n):
    idx = spread_indices(n, SAMPLE)
    syms = ", ".join(f":k{i:03d}" for i in idx)
    baked = sum(idx)  # sum of the sampled values (value == index)
    return syms, baked


def main():
    here = os.path.dirname(os.path.abspath(__file__))
    out = os.path.join(os.path.dirname(here), "020_lookup_curve.eu")

    keys40, sum40 = key_list(40)
    keys250, sum250 = key_list(250)
    total40 = M * sum40
    total250 = M * sum250

    doc = (
        '"Block lookup, large-static-literal benchmark (workload class E). '
        "GENERATED by gen/gen_020_lookup_curve.py -- do not edit by hand. "
        "Freezes the W3 lookup-evidence spike (report 2026-07-12-w3-lookup): "
        "static-literal blocks of 40 and 250 keys hit by saturated dynamic "
        "lookup (16 keys/iteration). The bytecode engine walks an interpreted "
        "linear find loop (index gated off); HeapSyn builds/reuses a key index "
        "(>=16-key threshold), so bc is dramatically slower here -- a "
        "regression tripwire for the index decision (eu-4zhi), not a "
        "real-workload win. EXCEPTION to the >1s-faster-engine floor: this "
        "bench's purpose is the bc/hs asymmetry itself; the indexed hs engine "
        "is intrinsically sub-second (~0.3s, still ~5x the startup floor, so "
        "the ratio is sound), and forcing it over 1s would push bc to ~15s and "
        'break the suite budget. bc sits ~2.5s. See PROTOCOL.md section 6."'
    )

    src = f"""{doc}

# --- static-literal lookup tables (index built once on HeapSyn) ---

` :suppress
big40: {block_literal(40)}

` :suppress
big250: {block_literal(250)}

# 16 keys spread across each table (average find depth ~N/2).
` :suppress
keys40: [{keys40}]

` :suppress
keys250: [{keys250}]

# One iteration: sum {SAMPLE} dynamic lookups into the block.
add-lookup(b, acc, k): acc + lookup(k, b)
sum-sample(b, ks, _r): ks foldl(add-lookup(b), 0)

# M distinct rounds (the ignored round index forces {M} separate
# evaluations rather than one memoised thunk), summed.
` :suppress
total40: range(0, {M}) map(sum-sample(big40, keys40)) sum

` :suppress
total250: range(0, {M}) map(sum-sample(big250, keys250)) sum

` {{ target: :bench-lookup-curve
    verify: ["default-expectation"] }}
bench-lookup-curve: {{
  n40-total: total40
  n250-total: total250
  RESULT: if(total40 = {total40},
             if(total250 = {total250}, :PASS, :FAIL),
             :FAIL)
}}
"""
    with open(out, "w") as f:
        f.write(src)
    print(f"wrote {out} (M={M}, sum40={sum40}, sum250={sum250})")


if __name__ == "__main__":
    main()
